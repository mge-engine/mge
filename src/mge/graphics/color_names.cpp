    {
        std::string namestr(name);
        boost::to_lower(namestr);
        for (size_t i = 0; i < sizeof(color_names) / sizeof(color_name); ++i) {
            if (namestr == color_names[i].n) {
                return &color_names[i];
            }
        }
        return 0;
    }

